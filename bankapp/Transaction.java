package com.assignment3.bankapp;

public class Transaction {
	
	private String transactionId;
	private double amount;
	private String type;
	
	public Transaction() {
		
	}
	public String getTransactionId() {
		return transactionId;
		
	}
	public double getAmount() {
		return amount;
		
	}
	public String getType() {
		return type;
	}

}
